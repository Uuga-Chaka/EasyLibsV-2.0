package icesi.dmi.easylibdos;

public class Coordenada {

    public int x;
    public int y;
    public int state;
    public String ids;

    public Coordenada() {

    }

    public Coordenada(int x, int y, int state, String ids) {
        this.x = x;
        this.y = y;
        this.state = state;
        this.ids = ids;
    }
}
